export interface ConfiguracaoIcones {
  [key: string]: string;
}

/**
 * Retorna o ícone para uma categoria específica
 * Usa o ícone customizado se disponível, caso contrário usa o padrão
 */
export function getIconeCategoria(
  categoria: TipoCategoria,
  iconesCustomizados?: ConfiguracaoIcones
): string {
  if (iconesCustomizados && iconesCustomizados[categoria]) {
    return iconesCustomizados[categoria];
  }
  return ICONES_PADRAO[categoria];
}

/**
 * Valida se um ícone é um SVG válido
 */
export function validarSvgIcone(svg: string): boolean {
  try {
    // Verificar se começa com <svg e termina com </svg>
    const trimmed = svg.trim();
    return trimmed.startsWith('<svg') && trimmed.endsWith('</svg>');
  } catch {
    return false;
  }
}
